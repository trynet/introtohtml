<h1>Appendix B: 
  <br>
Multi-tasking a/k/a Doing The Application Shuffle</h1>



<p>To make web pages for this class (this is what people call "making pages by
  hand" -
  that is, without the use of an application such as Dreamweaver), you have
  to get the hang of multi-tasking. This is the ability to use two applications
  at one time. That means using Notepad, the simplest of text editors (or TextEdit
  for the Mac), as one application and your browser as the other.</p>

<p>I've got <a href="../lab1.html#vid">two videos in Lab 1</a> that should help
  you out with all of this.</p>

<p>Open Notepad. Start by typing in your basic required tags into a new document. (You forgot the basics already? See <a href="../lesson1/10.html">Lesson
  1 - The Basics</a><A href="../lesson1/10.html"></A>.)</p>
<p>Now go ahead and save the file. Since this is the first save, the Save As dialog box appears. (Oh yeah, you do know how to Save a file, right?)</p>
<p>The File Type you'll want to choose is Plain Text or MS-DOS text or ASCII,
  but whatever you call it, there won't be much in the way of formatting because
  that is what you're going to be doing with the HTML tags.</p>
<p>Give the file a name, like hello.html, and save it to your Desktop. You have to use a file extension like .html (.htm is okay, but I use .html throughout). Why give it a file extension? It's going to be real hard to refer to a file when making a link if our files don't have extensions. That's one reason and there are others.</p>

<p>Where you save the file is not terribly important, but you need to remember where you saved it. As we are just getting underway, saving it to your Desktop is handy.</p>
<p>Okay, so now let's open the file from your Desktop, assuming you saved it there. And, assuming the .html file is associated with your default web browser, it opens in that browser.</p>
<p>Your file opens and what do you see?</p>
<p>Nothing, right? Just a blank page. Excellent!!</p>
<p>Okay. Notice now that I have both Notepad and the browser going. You can move
  them around, minimize them--do whatever--but keep them running because you
  need them open. Remember we are Multi-tasking. Two applications are sharing
  one file.</p>
<p>With your file open in Notepad, you can start adding the text between the <CODE>&lt;body&gt;</CODE> <CODE>&lt;/body&gt;</CODE> container. Suppose you wanted to say, "Hey, This Is Really Cool!!"</p>
<p>So, you'd type those words between <CODE>&lt;body&gt;</CODE> <CODE>&lt;/body&gt;</CODE> like this:</p>
<p><CODE>&lt;p&gt;</CODE>Hey, This is Really Cool!!<CODE>&lt;/p&gt;</CODE></p>
<p>Then you'd save your file by either going to your Menu Bar and choosing File -&gt; Save, or you might want to use the short cut keystrokes CTRL+S.</p>
<p>Go back to your browser. It's still open to your file, right? Just hit the
  Refresh (or Reload) button and what do you see? If all goes well, at the top
  and left of the browser window you'll see the words "Hey, This is Really Cool!!"</p>
<p><STRONG>By George Me Thinks You've Got It!!</STRONG></p>
<p>You're a Jedi Webmaster now. Try putting in another line of text below "Hey, This Is Really Cool!!" I'll leave it up to you, but when you are done, just Save your file again and move back to the browser. Hit the Refresh button again. See the changes you've made?</p>
<p>Now you're really dangerous, but you're on your way and you can see how Multi-tasking works. It's back and forth between the editor and the browser. Make your edits. Save the file. Refresh the browser. Make more edits and so on.</p>


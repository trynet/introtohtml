<h1>Appendix E: <br />
FTP Or How To Get Your Files From Here To There</h1>


  <p>You might be wondering, &quot;How do I get my files onto the web so that 
    others can see my web site?&quot; </p>
  <p>Joy Of Code: Intro To HTML's Work Space is fine for posting files to be
    reviewed by Your Friendly Web Design Instructor, but it's not your solution
    for getting your files from your computer to a web server.</p>
  <p>To do that, that is, to copy and move files from Point A (your local system) 
    to Point B (a web server), requires a special application known as FTP, short 
    for File Transfer Protocol.</p>
  <p><em>Transfer </em> is the operative word here, because FTP allows you to transfer 
    files from your computer to a server and vice versa.</p>
  <p>There are a lot of FTP applications out there, many of them free, or at least 
    trial ware, for the first 30 days.</p>
  <p>My pick, and that which is considered the gold standard for Windows users,
     is WS_FTP made by a small outfit called Ipswitch. (That's a neat play on
     Internet Protocol Switch, if you're interested.)</p>
  <p>Rather than give you a tutorial with screen shots of how WS _FTP works, here 
    are some resources that will help you get started with FTP.</p>
  <h2>Ipswitch and WS_FTP</h2>
  <p>See the <a href="http://ipswitch.com/index.asp">home page</a> of ipswitch.com
    for general information. </p>
  <p>You'll easily find all the information you need by poking around their site 
    including how to download, try, and purchase WS_FTP.</p>
  <h2>Other Resources</h2>
  <p>See <a href="http://www.ftpplanet.com/ftpresources/basics.htm">FTP 101 - 
    A Beginner's Guide</a> for more assistance. This <a href="http://www.ftpplanet.com/">FTP Planet</a> site is sponsored by Ipswitch, the people who know what they're 
    doing when it comes to moving files around.</p>
  <h2>Mac Users</h2>
  <p>The gold standard - IMHO - for the Mac is <a href="http://www.panic.com/transmit/">Transmit</a> 
    made by Panic. I love it!!</p>
  <p>Another well known program is Fetch. The idea is exactly the same as WS_FTP 
    but the interface is completely different. You're still transferring files 
    from one computer to another, but in this case it's from a Mac to a web server. 
  </p>

  <p>See <a href="http://www.fetchsoftworks.com/">Fetch Softworks</a> for the 
    application. You get a free 15 day trial.</p>
  <h2>Add End</h2>
  <p>To get your site on the web so people can view it, you need to get space 
    on a web server to store your files. I haven't said anything about how to get 
    an account with a web server (or web hosting company). That's a fairly big 
    topic since your options are enormous. Your budget and the technology requirements 
    for your site are but a few of the factors you'll need to consider in deciding 
    just who is going to host your bits.</p>
  <p>Once you get space on a server, that's when you'll need FTP!!</p>


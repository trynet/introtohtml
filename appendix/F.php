<h1>Appendix  F:<br />
Old World, New World</h1>





<p>So one day, in the middle of a lecture I was giving on web design at Pratt 
    Institute in New York City, I got distracted with an idea which has become 
    my &quot;Old World, New World&quot; rant. I stumbled on one huge idea that 
    made me realize, &quot;I got it.&quot; An epiphany.</p>
  <p>Put simply, the Old World is Print, a world of fixed dimensions. One where 
    space is divided by absolute sizes - inches, centimeters, fonts expressed 
    in points. A world that can be completely controlled by creatives and designers. 
    A world where 10,000 brochures are printed and they are all clones of each 
    other.</p>
  <p>A world adored by control freaks. A world without users.</p>
  <h2>The Electronic Canvas</h2>
  <p>This space, the electronic canvas, is the New World, the world of Computer-Mediated
     Communications. This world features a canvas which is proportional and scalable.
    Little involves absolute size. Measurement is almost always related to some
    other factor, that is,  measurement is indirect.</p>
  <p>Every display device - monitors, large projection screens and handhelds,
    to name a few - feature very different display parameters. Take the computer
    monitor. What one sees on screen will be controlled by the operating system,
    the type and size of the monitor, the browser itself and, most important,
    the screen resolution (the number of pixels measured
    horizontally and vertically) being used.</p>
  <p>If you think of your screen as a canvas, then the screen resolution (software
     built into an operating system) controls how big or small that canvas is.
     The electronic canvas has stretching - or some say liquid - properties.
    A  screen resolution of 640 X 480  renders a canvas which is 640 pixels (picture
    elements  or a unit of screen measurement) wide by 480 pixels high. A screen
    resolution that is 800 X 600, is 800 pixels wide by 600 pixels high. Another
    screen resolution, one that many people favor, is 1024 X 768.</p>
  <p>The lower the screen resolution numbers, the bigger things will appear
    on the screen. Go ahead, play around with display driver settings. On the
    Windows platform, get to Display Controls via the Control Panel, or by right
    clicking your mouse on the Desktop. In the Display window, select the Settings
    tab. 
    <strong><em>Note which screen resolution is being used</em></strong>, then
    go ahead and experiment by changing that setting. Once you have a different
    screen resolution selected (this is not hard to do), go surfing.</p>
  <p>What you'll find is that web pages look very different using different screen
    resolutions. Some pages are well designed - in my humble opinion - because
    no matter which screen resolution you use, the page looks great as it scales
    to the size of the canvas. But
    you'll find more pages that look good in only one resolution and waste
    screen real estate (space) because of an overuse of fixed fonts and other
    absolute sizing layout properties. This tells you one thing - the designers
    of that page are trapped in the Old World.</p>
  <p>Take a look at an inspirational read, &quot;<a href=
"http://www.drdobbs.com/article/printableArticle.jhtml;jsessionid=MTWXRRA5KLIA3QE1GHPSKH4ATMY32JVN?articleId=184412392&dept_url=/">The Myth 
    of 800x600</a>&quot;, by James Kalbach. He has an M.A. in Information Science 
    and is the Head of Information Architecture at Razorfish, Hamburg. I couldn't 
    explain it any better than he does - and he's a nice guy too!!</p>
  <h2>Design Philosophy</h2>
  <p>Think I'm talking philosophy? You better believe it. See John Allsop's "<a href=
"http://www.alistapart.com/articles/dao/">A Dao of Web Design</a>" 
    which looks at web design through the prism of the Tao Te Ching.</p>
  <p>Now don't get me wrong. I understand all too well the issues web designers
     and developers face in setting a typography that one can live with in the
     New World. Corporations have branded identities which need to be protected.
     If you say, "Don't set your font size to 12 pixels  because it makes
     little sense in this environment. Rather, set it to 100% of the user's font
     size preference," they will look at you as if you are from Mars.</p>
  <p>This Old World, New World paradigm is no trivial matter. Whole corporate 
    structures have been successfully built upon the Old World model. Take a well 
    known company I did some work for, whose roots are in book publishing (it 
    doesn't get more Old World than that). They were building an online bookselling 
    service and they were hellbent on making their web pages look like flyers 
    and brochureware, something with which they had a lot of experience. The web 
    site not only looked awful, it was completely hard coded, that is, unscalable 
    to the user's preferences. It looked bad in ANY display driver. However,it 
    was their corporate tradition, it was how they thought, how they were organized, 
    that caused them to show the world that they did not understand the New World. 
    And yes, all this user interface stuff has a huge effect on relations with 
    users, and whether the user's experience will be satisfying and lead to some 
    intended result.</p>
  <h2>The Universe Of Relativity</h2>
  <p>Compare that with how Amazon and eBay look and function, and you'll see examples 
    of consumer centric companies that know how to deliver great online experiences. 
    They understand the operating environment of the online world. They understand 
    this medium and work with it and not against it. That means knowing that this 
    is a universe of relative sizing and proportionality - relative and proportional 
    to preferences set by users in their browsers.</p>
  <p>I've gone on a bit long here. I don't expect you to get this all at once.
     These ideas lay at the foundation of my web design classes which are practical
     and conceptual. Don't fret or worry if this all does not come together in
     one second. It takes time. You may even struggle with it depending on your
     background. Me, I never came from the Old World, so it was easier but it
    still  took time for these ideas to sink in.</p>
  <p>There is much more to learn, and as you do, more of this will make sense - if 
    it hasn't already.</p>
		  
		          

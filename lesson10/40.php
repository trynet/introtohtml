 <h1>Five  Things I'm Dying To Tell You About CSS</h1>
 
 <p>I love CSS  - as if you didn't know. Even though I teach it, I still am very
   much a student of it as well. Over the years I've learned a few things about
   it that you should know especially if you are thinking of going on to learn
   it. Like I always say, &quot;Why bother to learn HTML if you never learn the
   fun and pretty stuff of web design.&quot;  That would be CSS.</p>
 
 <h2>It's Totally Different Than HTML But Without HTML It Means Nothing</h2>
 
 <p>It's not just that it has a different syntax as you've just seen. CSS operates
   on a totally different level.</p>
 <p>As you you've seen HTML is really straightforward. Once you know what the
    tags do all you need to do is use them to markup your content to give it
   meaning. Once
    in a while you have to make a command decision about which tag to use but
   it's usually pretty clear.</p>
 <p>Writing good CSS is another story. It takes time to understand how to work
   with the Browser and User Style Sheets and how to use inheritance (something
   I haven't talked about here) to its best advantage.
   Once you get good at it, you spend time thinking of what you will do before
   you do it. And there are lots of good ways (and bad ways) to write CSS. That's
   why I always say that CSS is a thinking person's game.</p>
<p>CSS is very contextual in how it operates but if it's not attached to HTML
  it has no context in which to operate.</p>
 
 <h2> CSS Is All About User-Centric Design </h2>
 <p>Unlike traditional media (that's the <a href="../appendix/F.html">Appendix
     F: Old World, New World</a> thing) which puts
   designers in the center of the design, web page design puts the user in the
   center of the design. </p>
 <p>Why do I know this like I know my name? Because it's true.</p>
 <p>Users have influence over design when it comes to electronic publishing. In
   this medium, users can - with any browser - zoom in and out on a page, shrink
   or expand the browser's viewable area (the viewport) and change the resolution
   of the screen.  These options change the appearance of a web page which, 
   if the designer does not take into consideration, will do so at her - or worse -
   her client's peril.</p>
 
 <h2>Everything  You Know About Style - And Styles You Don't Know - Is Set With
   CSS</h2>
 
 <p>It's not just about setting the typeface or the size of a font. Nor 
   the color of the font or the background color of a page. CSS - whether through
   the Browser, User or Designer Style Sheet - set all matters pertaining to
   style.</p>
 <p>White space surrounding design elements, layout and the use of imagery for
   design (remember, I talked about this in Lesson 6 - Images and Videos) are
   a few of the big things that CSS does. It does all the subtle things too -
   line height, word and letter spacing and the style of list markers are all
   part of what CSS can do.</p>

 
 <h2>CSS's Role In Design Will Only Grow Over Time</h2>
 <p>If you think all that CSS does is set the style specifications for  content
   viewed in a web browser you'd be missing much of what CSS does today
   - and tomorrow. There is CSS for print, CSS for mobiles, CSS for large projection
   systems, heck there might even be CSS for egg beaters one day. </p>
 <p>Its role in design will only  grow over time, which makes it something  worth
   learning now and for the future.</p>
 <h2>Your Ability To Work Well With CSS Is Directly Related To How Well You
   Know HTML</h2>
 <p>This is the Prime Directive of web design. Without a good knowledge of HTML
   you are going no where fast with CSS.  Because CSS rides along side HTML
   writing good HTML is the key to tapping all the great things you can do with
   CSS.</p>
 <p>Why did you learn how to error check your pages? Why did you learn about
   the importance of creating well formed documents? Why did I show you what
   an HTML outline is? </p>
 <p>Think of our building. If the HTML walls are not plumb (that's validation)
   your CSS wallpaper will not hang correctly.</p>

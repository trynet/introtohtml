<h1>A Little History About This Class</h1>

<p>What you are learning here is from an evolution that began in 1999. This is really the third iteration of this class.</p> 

<p>Way back then it was HTML4.01. That was the standard. Web pages used a lot of awful code doing things like using tables for page layout. We actually had a  tag which did a poor job of setting the font size, color and type face.</p> 

<p>We lived in the era  of tag soup. </p>

<p>Why?</p>

<p>CSS was just starting to be used, but it frustrated many a designer as it was inconsistently implemented from one browser to another.
This caused  HTML to be used to achieve a certain look on a page. Those that were adherents to web design standards, like me, could only talk in theoretical terms. Today it's a reality.</p>

<p>Then in about 2004 I launched the predecessor  to this class, Introduction To XHTML And CSS.  It was very good for it's time but it too was flawed. In the class, like this one, I emphasized the correct use of the tags and that all pages must be validated. But the course  was flawed. XHTML has been shown the door and my class, that was used by about 2,000 people online and in class, was put to bed in December 2011.</p>

<h2>Enter Intro To HTML</h2>

<p>This class is the culmination of everything I believe in about web design. Separating structure from style is no longer a theory as was years ago. It is now mainstream web page design and this approach reflects the maturity of HTML and CSS. There is no going back to the bad old days!!</p>

<p>Here, it's all HTML all the time. You will see what CSS does at the end, but in this class you will learn how to use HTML the right way and set yourself up to learn CSS. Your ability to work well with CSS is directly connected to how well you know HTML as CSS is a companion to HTML.</p>


<p>HTML has matured to the point where I believe this just might be the last major overhaul I will ever need to do for this class.  At least I hope so!! </p>



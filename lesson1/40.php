<h1>The DOCTYPE Statement</h1>

<p>When an HTML file starts to load in the browser, one of the first

          things the browser says is, &quot;Hey, what do I have here? What kind

          of file is this?&quot;</p>

        <p>The DOCTYPE statement tells the browser,

at the top (literally) what

          type of file is coming in.</p>



        
<p>With HTML 5 the DOCTYPE Statement could not be easier (in case you were familiar
  with  XHTML and older versions of HTML).</p>



  <p><code>&lt;!DOCTYPE html&gt;</code></p>



        <p>That's all you need to put a the very top of all your HTML files.</p>



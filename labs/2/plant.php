<h2>Getting Started</h2>

<p>When I talk about "laying out the garden" before everything is planted it's just my way to organize what will go where.  After all these years I know what plants, like tomatoes,  need the most sunlight and which ones, like zucchinis, can stand the shade.</p>

 <p>This gives you a rough idea where the plants go.  Give your plants as much space as you can.  Overcrowding will give you a very poor yield.  Roots need space to grow and so do the leafy parts of your plants.</p>


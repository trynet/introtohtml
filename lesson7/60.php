 <h1>Data Validation</h1>

 <p>What I'm about to say goes outside the bounds of our class, but I feel
   the need to make you aware of Data Validation.</p>
 <p>Data validation is the process by which the data that is supplied by the
   user is checked before it is submitted to the server.</p>
 <p>Checked? Checked for what? Frequently, it's a JavaScript program that will
   check, at the time the data is being submitted to the server, to see that
   the information makes sense and is in the proper format. For example, ever
   get a pop-up alert box that said something like &quot;You've Entered An Invalid
   Email Address&quot; or &quot;Your Password Must Contain At Least 8 Characters?&quot; That's
   a lot of what data validation is about.</p>
 <p>The idea is that the information is screened before it comes to the server.


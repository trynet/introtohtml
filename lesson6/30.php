<h1>How Images Are Used In Web Design</h1>
<p>There are two ways images are used in web design. The differences are significant
  and pretty easy to understand.</p>
<p>I'm about to show you the markup needed to embed (insert) an image in a web
  page.</p>
<p>An embedded image, which is what this lesson is about, is used when the image
  is relevant to the content of the page. For example, if your page is about
  the Roman Coliseum then you'll want to embed some images of the Coliseum. Or,
  if your page is about your child's soccer team then an embedded image  of junior's
   soccer team would be most appropriate.</p>
<h2>What About Background Images?</h2>
<p>The other way images are used are as background decorative items.</p>


        
<p>You can use images as backgrounds to any HTML element.   But since here we
  are talking about the appearance of a web page - it's art direction - those
  matters of style fall under the purview of CSS and are not taken up in this class.
  But the use of background images is certainly a big topic in my <a href="http://www.joyofcode.com/online-web-design-classes/practical-css">Joy
  Of Code: Practical CSS</a>  class.</p>
  
  
 
